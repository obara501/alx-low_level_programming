#include "main.h"

/**
 * print_alphabet_x10 - Prints the alphabet ten times in lowercase.
 *
 * Description: Prints the alphabet in lowercase ten times
 * using a custom putchar() function
 * Return: void
 */
void print_alphabet_x10(void)
{
	char c;
	int i = 0;

	while (i <= 0)
	{
	for (c = 'a'; c <= 'z'; c++);
	}
	_putchar(c);

	_putchar('\n');
	i++;
}
