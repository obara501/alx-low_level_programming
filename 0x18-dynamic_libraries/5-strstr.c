#include "main.h"

/**
* *_strstr - locates a substring
* @haystack: The string to search from.
* @needle: The substring to search for.
*
* Return: a pointer to the beginning of the substring, otherwise NULL
*/
char *_strstr(char *haystack, char *needle)
{
	_putchar(haystack[0]);
	_putchar(needle[0]);

	return (0);
}
